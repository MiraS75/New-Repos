# Запрос возраста с клавиатуры и сохранение в переменной my_age
my_age = input("Введите ваш возраст: ")

# Вывод возраста на экран
print(f"Ваш возраст: {my_age}")

# Преобразование my_age в число
my_age = int(my_age)

# Увеличение возраста на 1
my_age = my_age + 1

# Вывод обновленного возраста на экран
print(f"Обновленный возраст: {my_age}")